package adminaction;

public class PointStatDataBean {
	private String p_date;
	private int chcount;
	private int excount;
	private int chprice;
	private int exprice;
	
	public String getP_date() {
		return p_date;
	}
	public void setP_date(String p_date) {
		this.p_date = p_date;
	}
	public int getChcount() {
		return chcount;
	}
	public void setChcount(int chcount) {
		this.chcount = chcount;
	}
	public int getExcount() {
		return excount;
	}
	public void setExcount(int excount) {
		this.excount = excount;
	}
	public int getChprice() {
		return chprice;
	}
	public void setChprice(int chprice) {
		this.chprice = chprice;
	}
	public int getExprice() {
		return exprice;
	}
	public void setExprice(int exprice) {
		this.exprice = exprice;
	}
	
	
}
