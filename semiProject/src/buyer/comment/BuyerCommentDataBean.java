package buyer.comment;

public class BuyerCommentDataBean {
	private int bc_num;
	private int b_num;
	private String bc_writer;
	private String bc_comment;
	private String bc_reg_date;
	
	public int getBc_num() {
		return bc_num;
	}
	public void setBc_num(int bc_num) {
		this.bc_num = bc_num;
	}
	public int getB_num() {
		return b_num;
	}
	public void setB_num(int b_num) {
		this.b_num = b_num;
	}
	public String getBc_writer() {
		return bc_writer;
	}
	public void setBc_writer(String bc_writer) {
		this.bc_writer = bc_writer;
	}
	public String getBc_comment() {
		return bc_comment;
	}
	public void setBc_comment(String bc_comment) {
		this.bc_comment = bc_comment;
	}
	public String getBc_reg_date() {
		return bc_reg_date;
	}
	public void setBc_reg_date(String bc_reg_date) {
		this.bc_reg_date = bc_reg_date;
	}
	
	
}
