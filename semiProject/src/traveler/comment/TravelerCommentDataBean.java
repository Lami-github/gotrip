package traveler.comment;

public class TravelerCommentDataBean {
	private int trc_num;
	private int tr_num;
	private String trc_writer;
	private String trc_comment;
	private String trc_reg_date;
	
	public int getTrc_num() {
		return trc_num;
	}
	public void setTrc_num(int trc_num) {
		this.trc_num = trc_num;
	}
	public int getTr_num() {
		return tr_num;
	}
	public void setTr_num(int tr_num) {
		this.tr_num = tr_num;
	}
	public String getTrc_writer() {
		return trc_writer;
	}
	public void setTrc_writer(String trc_writer) {
		this.trc_writer = trc_writer;
	}
	public String getTrc_comment() {
		return trc_comment;
	}
	public void setTrc_comment(String trc_comment) {
		this.trc_comment = trc_comment;
	}
	public String getTrc_reg_date() {
		return trc_reg_date;
	}
	public void setTrc_reg_date(String trc_reg_date) {
		this.trc_reg_date = trc_reg_date;
	}
	
	
}
